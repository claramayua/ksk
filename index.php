						<p>Kechara Soup Kitchen (KSK) is a non-religious community action group that distributes food, basic medical aid and counselling to the homeless and urban poor of Malaysia. Founded by His Eminence Tsem Rinpoche, the Spiritual Guide of the Kechara organization, KSK is strictly non-religious, being mindful and respectful of others’ beliefs. KSK do not discriminate in terms of race, gender or culture. Its motto is “Hunger Knows No Barriers”. For this reason, its food is vegetarian.</p>

            <p>Kechara Soup Kitchen (KSK) works towards the mission of ending hunger and reducing poverty. KSK began as a small Non-Governmental Organization that provided warm and nutritious vegetarian meals to the homeless. KSK has also developed food bank initiatives that collects surplus provisions from grocers and manufacturers which will be rechannelled to needy families. KSK currently collects up to 60 tons of surplus food every month.</p>

